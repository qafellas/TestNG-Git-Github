package org.qafellas.function;

public class ArithmeticFunctions {
    public int add(int x, int y){
        return x + y;
    }

    public int subtract(int x, int y){
        return x - y;
    }

    public int multiply(int x, int y){
        return x * y;
    }

    public double division(double x, double y){
        return x / y;
    }
}
